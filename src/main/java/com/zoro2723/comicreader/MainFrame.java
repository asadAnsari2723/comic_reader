    private void startPreprocessing() {
        ExecutorService executorService = Executors.newFixedThreadPool(4);  // Create a thread pool
        Connection con = DatabaseConnection.connect();

        if (!TableCheck.doesTableExist(con, "zip_files"))
            TableCheck.createTables(con);
        if (!TableCheck.doesTableExist(con, "image_previews"))
            TableCheck.createTables(con);
        
        for (Component item : filePanel.getRightPanel().getComponents()) {
            if (item instanceof FileItemComponent) {
                // zipProcessList.add(new ZipPreprocessor((FileItemComponent)item, con));
                executorService.submit(new ZipPreprocessor((FileItemComponent) item, con));
            }
        }
        
        executorService.shutdown();  // Shut down the executor after all tasks are submitted

    }

    private static FileList<File> initialize() {
        FileList<File> fileList;
        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream("fileList.dat"))) {
            fileList = (FileList<File>) ois.readObject(); // 'fileList' is your FileList instance
            System.out.println("FileList loaded successfully.");
        } catch (IOException | ClassNotFoundException e) {
            System.err.println("Error loading FileList: " + e.getMessage());
            // Handle the case where the file doesn't exist (e.g., first launch)
            fileList = new FileList<>(); // Create a new FileList if the file is not found
        }
        return fileList;
    }

