    private void openMIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openMIActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Zip Files", "zip"));
        fileChooser.setDialogTitle("Select a Zip File");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        fileChooser.setMultiSelectionEnabled(true);
        int returnValue = fileChooser.showOpenDialog(null); // For opening files
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            fileList.addAll(fileChooser.getSelectedFiles());
            filePanel.addFiles();
        }

    }//GEN-LAST:event_openMIActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing

        try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("fileList.dat"))) {
            oos.writeObject(fileList); // 'fileList' is your FileList instance
            System.out.println("FileList saved successfully.");
        } catch (IOException e) {
            e.printStackTrace();
            System.err.println("Error saving FileList: " + e.getMessage());
        }

    }//GEN-LAST:event_formWindowClosing

